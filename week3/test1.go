package main

import "fmt"

func main() {
	fmt.Printf("%v \n", 17)
	fmt.Printf("%d \n", 18)
	fmt.Printf("%g \n", 19.259)
	fmt.Print("%s \n", "hello")
	fmt.Println("%s \n", "hello")
	fmt.Printf("%s \n", "hello")
	fmt.Printf("%#v \n", "17")
	fmt.Printf("%#v \n", 17)
	fmt.Printf("%T \n", "20")
	fmt.Printf("%T \n", 20)
	fmt.Printf("%T \n", 20.22)
	fmt.Printf("%% \n")
	fmt.Printf("%v%% \n ", 20)
	fmt.Printf("%v%% \n", 20.568)
	fmt.Printf("%t \n", 17 > 18)
	fmt.Printf("%t \n", 20 < 22)
	fmt.Printf("%S  \n", "\"string\"")

}
